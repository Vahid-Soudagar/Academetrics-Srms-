    private void saveResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveResultActionPerformed
        // TODO add your handling code here:
        String rollNo = this.rollNo.getText();
        String s1 = this.s1.getText();
        String s2 = this.s2.getText();
        String s3 = this.s3.getText();
        String s4 = this.s4.getText();
        String s5 = this.s5.getText();
        
        try {
            Connection c = DriverManager.getConnection(URL+DATABASE_NAME, USER, PASSWORD);
            String getStudentQuery = " SELECT * FROM "+ STUDENT_TABLE+" WHERE rollNo = ?";
            PreparedStatement preparedStatement = c.prepareStatement(getStudentQuery);
            preparedStatement.setString(1, rollNo);
            ResultSet resultSet = preparedStatement.executeQuery();
            if (resultSet.next()) {
                String insertIntoResult = "INSERT INTO "+RESULT_TABLE+
                        "(rollno, imperativeProgramming, digitalElectronics, maths, operatingSystem, communicationSkills) "+
                        " VALUES (?,?,?,?,?,?)";
                PreparedStatement preparedStatement1 = c.prepareStatement(insertIntoResult);
                preparedStatement1.setString(1, rollNo);
                preparedStatement1.setString(2, s1);
                preparedStatement1.setString(3, s2);
                preparedStatement1.setString(4, s3);
                preparedStatement1.setString(5, s4);
                preparedStatement1.setString(6, s5);
                int row = preparedStatement1.executeUpdate();
                if (row > 0) {
                    JOptionPane.showMessageDialog(null, "Resukt added successfully");
                    setVisible(false);
                    new AdminHome().setVisible(true);
                    System.out.println("Values inserted");
                } else {
                    JOptionPane.showMessageDialog(null, "Insertion Failed");
                    System.out.println("Insertion failed");
                }
            } else {
                 JOptionPane.showMessageDialog(null, "No Student found");
            }
        } catch (Exception e) {
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "Insertion Failed "+e.getMessage());
        }
        
    }//GEN-LAST:event_saveResultActionPerformed

