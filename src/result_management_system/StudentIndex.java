    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        String rollNO = searchField.getText();
        try {
            Connection c = DriverManager.getConnection(URL+DATABASE_NAME, USER, PASSWORD);
            String query = " SELECT * FROM "+STUDENT_TABLE+" WHERE rollNo = ?";
            PreparedStatement preparedStatement = c.prepareStatement(query);
            preparedStatement.setString(1, rollNO);
            ResultSet resultSet = preparedStatement.executeQuery();
            if (resultSet.next()) {
                JOptionPane.showMessageDialog(null, "Student found");
                setVisible(false);
                new StudentHome(rollNO).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Student not found or invalid roll number");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_searchButtonActionPerformed

